package day24;

public class ExecutingConstructors {
    public static void main(String[] args) {
        ConstructorsConcept cc = new ConstructorsConcept(); //call the constructor
        System.out.println(cc.b);
        System.out.println(cc.s);
        System.out.println(cc.i);
        System.out.println(cc.l);
        System.out.println(cc.f);
        System.out.println(cc.d);
        System.out.println(cc.c);
        System.out.println(cc.bl);
        System.out.println(cc.str);
    }
}
